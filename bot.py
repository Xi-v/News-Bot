# Function to parse through articles XML file
def fetch_articles(url): # url parameter referenced in the /newsround command
    headers = {'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64)'} # Fake user agent ig
    response = requests.get(url, headers=headers)
    soup = BeautifulSoup(response.text, 'xml') # uses bs4 to make it pretty
    return soup.find_all('item')[:5]

# Function to read that parsed data, and add it to a 2D list so that it can be used for sending messages
def read_articles(news_articles, source):
    title_array = []
    link_array = []
    summary_array = []
    news_round_array = []

    for article in news_articles:
        title = article.find('title').get_text(strip=True)

        # Cuz bbc and hachker news have different things gotta separate them
        if source == "bbc":
            summary = article.find('description').get_text(strip=True)
            link = article.find('guid').get_text(strip=True).split('#')[0]
        elif source == "hn":
            summary = f"Points: {article.find('description').get_text(strip=True).split('Points:')[1].split('<')[0].strip()}" # icl AI did this, cuz i got no clue wtf this does
            link = article.find('link').get_text(strip=True)

        title_array.append(title)
        summary_array.append(summary)
        link_array.append(link)

    # Appens all to one big list
    news_round_array.append(title_array)
    news_round_array.append(summary_array)
    news_round_array.append(link_array)
    return news_round_array

# shi to get the weather temp and description
async def _fetch_weather(city):
    async with python_weather.Client(unit=python_weather.METRIC) as client:
        weather = await client.get(city)
        return {
            "city": city,
            "temperature": weather.temperature,
            "description": weather.description,
        }
    

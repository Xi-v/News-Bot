# News Round feature, currently supports hacker news & BBC News
@tree.command(name="newsround", description="Top 5 stories on BBC News World")
@app_commands.describe(source="Choose your news source")
@app_commands.choices(source=[
    app_commands.Choice(name="BBC World News", value="bbc"),
    app_commands.Choice(name="Hacker News", value="hn")
])
async def newsround(interaction: discord.Interaction, source: app_commands.Choice[str]):
    # Pick between different news sourcespip install python-weather
    if source.value == "bbc":
        url = "https://feeds.bbci.co.uk/news/world/rss.xml"
        website_name = "BBC World News"
    elif source.value == "hn":
        url = "https://hnrss.org/frontpage"
        website_name = "Hacker News Front Page"

    articles = fetch_articles(url)
    news_round_array = read_articles(articles, source.value)

    # Message
    message = f"# {interaction.user.mention} Here is your News Round!\n"
    message += f"## {website_name}:\n" # Displays which source they choose, as if they didn't already inclue a parameter (ts only for aesthetics)

    # for loop to iterate through the 2D list generated by news_round_array
    for index in range(len(news_round_array[0])):
        message += f"### {news_round_array[0][index]}\n"
        message += f"{news_round_array[1][index]}\n"
        message += f"[Read More]({news_round_array[2][index]})\n"
    await interaction.response.send_message(message, suppress_embeds = True)

